#!/usr/bin/env node
/**
 * Generate .dev.vars for `pnpm cf:preview` from .env.local.
 * Wrangler does not read .env.local — it only loads .dev.vars.
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = process.cwd();
const envLocalPath = resolve(root, ".env.local");
const devVarsPath = resolve(root, ".dev.vars");

if (!existsSync(envLocalPath)) {
  console.error(
    "Missing .env.local. Copy .env.example to .env.local and fill in values first.",
  );
  process.exit(1);
}

const env = {};
for (const line of readFileSync(envLocalPath, "utf8").split("\n")) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("#")) continue;
  const eq = trimmed.indexOf("=");
  if (eq === -1) continue;
  const key = trimmed.slice(0, eq).trim();
  let value = trimmed.slice(eq + 1).trim();
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1);
  }
  env[key] = value;
}

const previewOrigin = "http://localhost:8787";
const keys = [
  "SESSION_SECRET",
  "SPLITWISE_CLIENT_ID",
  "SPLITWISE_CLIENT_SECRET",
];

const missing = keys.filter((key) => !env[key]?.trim());
if (missing.length > 0) {
  console.error(`Missing in .env.local: ${missing.join(", ")}`);
  process.exit(1);
}

if ((env.SESSION_SECRET?.trim().length ?? 0) < 32) {
  console.error("SESSION_SECRET in .env.local must be at least 32 characters.");
  process.exit(1);
}

const lines = [
  "# Generated by pnpm cf:sync-vars — do not commit",
  "NEXTJS_ENV=development",
  "",
  `APP_URL=${previewOrigin}`,
  `NEXT_PUBLIC_APP_URL=${previewOrigin}`,
  `SPLITWISE_REDIRECT_URI=${previewOrigin}/api/auth/splitwise/callback`,
  "",
  `SESSION_SECRET=${env.SESSION_SECRET}`,
  `SPLITWISE_CLIENT_ID=${env.SPLITWISE_CLIENT_ID}`,
  `SPLITWISE_CLIENT_SECRET=${env.SPLITWISE_CLIENT_SECRET}`,
  "",
];

if (env.DATABASE_URL?.trim()) {
  lines.push(`DATABASE_URL=${env.DATABASE_URL}`, "");
}

writeFileSync(devVarsPath, lines.join("\n"));
console.log(`Wrote ${devVarsPath} (preview URLs use ${previewOrigin}).`);
console.log(
  "Add http://localhost:8787/api/auth/splitwise/callback to your Splitwise app if not already set.",
);
